    public void load()
    {
        int a;
        
        try {
            pst = con.prepareStatement("select * from members");
            rs = pst.executeQuery();
            
            ResultSetMetaData rd = rs.getMetaData();
            a = rd.getColumnCount();
            df = (DefaultTableModel)tbl_members.getModel();
            df.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                for(int i=1; i<=a; i++)
                {
                    v2.add(rs.getString("MemberID"));
                    v2.add(rs.getString("Name"));
                    v2.add(rs.getString("NIC"));
                    v2.add(rs.getString("Address"));
                    v2.add(rs.getString("Contact"));
                    v2.add(rs.getString("Email"));
                    v2.add(rs.getString("Date"));
                }
                df.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Community.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

