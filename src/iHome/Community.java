    private void btn_regActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_regActionPerformed
        try {
            // TODO add your handling code here:
            String name = txt_name.getText();
            String nic = txt_nic.getText();
            String address = txt_address.getText();
            String contact = txt_contact.getText();
            String email = txt_email.getText();
            
            if(name.equals("")){
                lbl_name.setText("Name cannot be empty");
            }
            else if(!name.matches("[a-zA-Z]+")){
                lbl_name.setText("Name cannot accept");
            }
            else{
                lbl_name.setText("");
                
                if(nic.length()!=10){
                    lbl_nic.setText("NIC No cannot be empty and should be valid");
                }
                else{
                    lbl_nic.setText("");
                    
                    if(address.equals("")){
                        lbl_address.setText("Address cannot be empty");
                    }
                    else{
                        lbl_address.setText("");
                        
                        if(contact.length()!=10){
                            lbl_contact.setText("Contact No length should be 10 digits");
                        }
                        else if(!contact.matches("^[0-9]*$")){
                            lbl_contact.setText("Contact No length should be 10 digits");
                        }
                        else{
                            lbl_contact.setText("");
                            
                            if(email.equals("")){
                                lbl_email.setText("Email cannot be empty");
                            }
                            else if(!email.matches("^(.+)@(.+)$")){
                                lbl_email.setText("Email is not valid");
                            }
                            else{
                                lbl_email.setText("");
                                
                                pst = con.prepareStatement("Insert into members(Name,NIC,Address,Contact,Email) values(?,?,?,?,?)");
                                pst.setString(1, name);
                                pst.setString(2, nic);
                                pst.setString(3, address);
                                pst.setString(4, contact);
                                pst.setString(5, email);
                                pst.executeUpdate();
                                JOptionPane.showMessageDialog(this, "Member Registered");

                                txt_name.setText("");
                                txt_nic.setText("");
                                txt_address.setText("");
                                txt_contact.setText("");
                                txt_email.setText("");
                                txt_name.requestFocus();
            
                                load();
                            }
                        }
                    }
                }
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Community.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_regActionPerformed

    private void btn_delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delActionPerformed
         // TODO add your handling code here:
         df = (DefaultTableModel)tbl_members.getModel();
         
         int  selected = tbl_members.getSelectedRow();
         
         int id = Integer.parseInt(df.getValueAt(selected, 0).toString());
         
        try {
            pst = con.prepareStatement("delete from members where MemberID = ?");
            pst.setInt(1, id);
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Member Deleted");
            
            load();
            
        } catch (SQLException ex) {
            Logger.getLogger(Community.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_delActionPerformed

